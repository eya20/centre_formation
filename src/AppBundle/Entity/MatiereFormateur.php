<?php

namespace AppBundle\Entity;

/**
 * MatiereFormateur
 */
class MatiereFormateur
{
    /**
     * @var integer
     */
    private $matiereFormateurId;

    /**
     * @var \AppBundle\Entity\Formateur
     */
    private $formateur;

    /**
     * @var \AppBundle\Entity\Matiere
     */
    private $matiere;


    /**
     * Get matiereFormateurId
     *
     * @return integer
     */
    public function getMatiereFormateurId()
    {
        return $this->matiereFormateurId;
    }

    /**
     * Set formateur
     *
     * @param \AppBundle\Entity\Formateur $formateur
     *
     * @return MatiereFormateur
     */
    public function setFormateur(\AppBundle\Entity\Formateur $formateur = null)
    {
        $this->formateur = $formateur;
    
        return $this;
    }

    /**
     * Get formateur
     *
     * @return \AppBundle\Entity\Formateur
     */
    public function getFormateur()
    {
        return $this->formateur;
    }

    /**
     * Set matiere
     *
     * @param \AppBundle\Entity\Matiere $matiere
     *
     * @return MatiereFormateur
     */
    public function setMatiere(\AppBundle\Entity\Matiere $matiere = null)
    {
        $this->matiere = $matiere;
    
        return $this;
    }

    /**
     * Get matiere
     *
     * @return \AppBundle\Entity\Matiere
     */
    public function getMatiere()
    {
        return $this->matiere;
    }
}
